namespace Unity.WebRTC
{
    /// <summary>
    /// Specifies the bit depth for a render texture's depth buffer.
    /// </summary>
    public enum RenderTextureDepth
    {
        /// <summary>
        /// 16-bit depth buffer.
        /// </summary>
        Depth16 = 16,

        /// <summary>
        /// 24-bit depth buffer.
        /// </summary>
        Depth24 = 24,

        /// <summary>
        /// 32-bit depth buffer.
        /// </summary>
        Depth32 = 32,
    }
}
